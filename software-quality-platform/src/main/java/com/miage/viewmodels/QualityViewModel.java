package com.miage.viewmodels;

/**
 *
 * @author Tamer
 */
public class QualityViewModel {

    public String label;
    public Integer value;

    public QualityViewModel() {
    }

    public QualityViewModel(String label, Integer value) {
        this.label = label;
        this.value = value;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }

}
